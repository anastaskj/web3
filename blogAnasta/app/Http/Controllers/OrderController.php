    public function update(Request $request, $id)
    {
        $user = Auth::user();
        $order = DB::table('orders')->where('order_id', $id)->first();
        if($order->customer_id == $user->id)
        {
            DB::table('orders')->where('order_id', $id)->update(['completed' => 1, 'updated_at' => date('Y-m-d H:i:s')]);
            return redirect('/dashboard')->with('success', 'Delivery Updated!');
        }
        else
        {
            return redirect('/dashboard')->with('error', 'This is not your order!');
        }
    }

